package JavaBasic;
public class BonusWillBeGivenOrNot {

	public static void main(String[] args) {

	int joiningyear,currentyear,yearofservice;

	joiningyear=2015;

	currentyear=2022;

	yearofservice=currentyear-joiningyear;

	if(yearofservice>=3)

	System.out.println("Year Of Service is "+yearofservice+" so 2500 will be given to employee");

	else

	System.out.println("Year Of Service is "+yearofservice+" so 2500 will not be given to employee");
 
    }

  
}


