//java program to print Multiplication of floating point number
    class Multiply {


    public static void main(String[] args) {


       //f is to ensures that numbers are float Data Type
        float first = 2.5f;

        float second = 3.5f;


       //to store the multiplied value
        float product = first * second;


       //to print the multiply of Two Number
        System.out.println("The multiply of Two Number is: " + product);
}

    }